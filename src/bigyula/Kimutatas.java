    private void jTF_ArfolyamKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_ArfolyamKeyTyped
        char c = evt.getKeyChar();
        
        boolean startsWithPoint = jTF_Arfolyam.getText().length() == 0 && c == '.';
        boolean startsWithZero = jTF_Arfolyam.getText().length() == 0 && c == '0';
        boolean alreadyContainsPoint = jTF_Arfolyam.getText().contains(".") && c == '.';
        if (startsWithPoint || startsWithZero || alreadyContainsPoint) {
            evt.consume();
        }
        
        boolean NaN = c < '0' || c > '9'; // NaN = not a number (JS)
        boolean notBackspace = c != KeyEvent.VK_BACKSPACE;
        boolean notPoint = c != '.';
        if (NaN && notBackspace && notPoint) {
            evt.consume();
        }
    }//GEN-LAST:event_jTF_ArfolyamKeyTyped

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            MySQL mysql = new MySQL();
            String[] ertekek = {};
            PreparedStatement psta = mysql.getStatement("SELECT SUM(eladas) FROM kimutatas;", ertekek);
            ResultSet rs = psta.executeQuery();
            rs.next();
            jTF_Osszeladas.setText(String.valueOf(rs.getInt("SUM(eladas)")));
            
            psta = mysql.getStatement("SELECT * FROM kimutatas;", ertekek);
            rs = psta.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel)jT_Eladasok.getModel();
            while (rs.next()) {
                dtm.addRow(new Object[] {rs.getString(1), rs.getInt(2)});
            }
            
            
            psta.close();
            mysql.finalize();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Hiba a felhasználó felvételekor: " + ex.getMessage());
        }
        
        
    }//GEN-LAST:event_formWindowOpened

