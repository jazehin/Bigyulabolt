    private void jTF_OsszeladasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_OsszeladasKeyTyped
        char c = evt.getKeyChar();
        if (jTF_Osszeladas.getText().length() == 0 && c == '0') {
            evt.consume();
        }
        if ((c < '0' || c > '9') && c != KeyEvent.VK_BACKSPACE) {
            evt.consume();
        }
    }//GEN-LAST:event_jTF_OsszeladasKeyTyped

    private void jBtn_AtvaltasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_AtvaltasActionPerformed
        if (jTF_Osszeladas.getText().equals("") || jTF_Arfolyam.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Üres mező nem lehet!");
            return;
        }
        
        double osszeladas;
        double arfolyam;
        
        try {
            osszeladas = Double.parseDouble(jTF_Osszeladas.getText());
            arfolyam = Double.parseDouble(jTF_Arfolyam.getText());
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Hiba a bevitt adatokban: " + e.getMessage());
            return;
        }
        
        if (arfolyam <= 0) {
            JOptionPane.showMessageDialog(null, "Nulla és annál kisebb nem lehet az árfolyam!");
            return;
        }

        jTF_Euro.setText(String.format("%.2f", osszeladas / arfolyam));
    }//GEN-LAST:event_jBtn_AtvaltasActionPerformed

